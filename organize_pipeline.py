#!/usr/bin/env python3
"""
organize_pipeline.py
Creates the complete 16-phase, 68-module pipeline folder structure
with relative symlinks to actual data files in output/.
"""

import os
import shutil
from pathlib import Path

ROOT = Path(__file__).resolve().parent
PIPELINE = ROOT / "pipeline"
OUTPUT = ROOT / "output"

stats = {"dirs_created": 0, "symlinks_created": 0, "missing_sources": []}


def ensure_dir(path):
    if not path.exists():
        path.mkdir(parents=True, exist_ok=True)
        stats["dirs_created"] += 1


def make_symlink(target: Path, link: Path):
    if link.is_symlink():
        link.unlink()
    if target.exists() or target.is_dir():
        rel = os.path.relpath(target, link.parent)
        os.symlink(rel, link)
        stats["symlinks_created"] += 1
    else:
        stats["missing_sources"].append(str(target))


def link_specific_files(data_dir: Path, output_subdir: str, files: list):
    src_dir = OUTPUT / output_subdir
    for f in files:
        make_symlink(src_dir / f, data_dir / f)


def link_all_data_files(data_dir: Path, output_subdir: str, prefix: str = ""):
    src_dir = OUTPUT / output_subdir
    if not src_dir.is_dir():
        stats["missing_sources"].append(str(src_dir))
        return
    for f in sorted(src_dir.iterdir()):
        if f.name in ("cache", "__pycache__", ".DS_Store", "checkpoint.json"):
            continue
        if f.is_dir():
            continue
        link_name = f"{prefix}{f.name}" if prefix else f.name
        make_symlink(f, data_dir / link_name)


def link_courses(d: Path):
    link_specific_files(d, "02_liste_courses", [
        "courses_normalisees.json", "partants_normalises.json"
    ])

def link_features(d: Path):
    link_specific_files(d, "features", [
        "features_matrix.csv", "features_matrix.json", "features_matrix.parquet"
    ])

def link_labels(d: Path):
    link_specific_files(d, "labels", [
        "labels.csv", "labels.json", "labels.parquet"
    ])

def link_rapports_merged(d: Path):
    link_specific_files(d, "rapports_merged", ["rapports_complets.json"])

def link_cotes(d: Path):
    link_specific_files(d, "07_cotes_marche", [
        "cotes_marche.csv", "cotes_marche.json", "cotes_marche.parquet"
    ])

def link_performances(d: Path):
    link_specific_files(d, "22_performances_detaillees", [
        "performances_detaillees.json"
    ])

def link_hippodromes(d: Path):
    make_symlink(ROOT / "hippodromes_db.py", d / "hippodromes_db.py")

def link_meteo(d: Path):
    link_specific_files(d, "13_meteo_historique", [
        "meteo_historique.csv", "meteo_historique.json", "meteo_historique.parquet"
    ])


def create_placeholder(data_dir: Path, name: str):
    sub = data_dir / name
    ensure_dir(sub)
    gitkeep = sub / ".gitkeep"
    if not gitkeep.exists():
        gitkeep.touch()


def write_readme(data_dir: Path, lines: list):
    readme = data_dir / "README.txt"
    content = ["Data sources for this module:", "=" * 40, ""]
    for line in lines:
        content.append(f"  - {line}")
    content.append("")
    content.append("All paths are relative symlinks to output/.")
    content.append("Generated by organize_pipeline.py")
    readme.write_text("\n".join(content), encoding="utf-8")


def module_dir(phase: str, module_name: str) -> Path:
    d = PIPELINE / phase / module_name / "data"
    ensure_dir(d)
    return d


