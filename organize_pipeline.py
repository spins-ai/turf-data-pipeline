def main():
    # Step 1: Remove existing pipeline/data/
    old_data = PIPELINE / "data"
    if old_data.exists():
        shutil.rmtree(old_data)
        print(f"[REMOVED] {old_data}")

    # Step 2: Build the full structure
    print(f"Project root: {ROOT}")
    print(f"Building 16-phase, 68-module pipeline structure...\n")
    build_all()

    # Step 3: Count modules
    module_count = 0
    phase_count = 0
    for phase_dir in sorted(PIPELINE.iterdir()):
        if phase_dir.is_dir() and phase_dir.name.startswith("phase_"):
            phase_count += 1
            for mod_dir in sorted(phase_dir.iterdir()):
                if mod_dir.is_dir():
                    module_count += 1

    # Step 4: Summary
    print("=" * 60)
    print("  PIPELINE STRUCTURE CREATION SUMMARY")
    print("=" * 60)
    print(f"  Phases:               {phase_count}")
    print(f"  Modules:              {module_count}")
    print(f"  Directories created:  {stats['dirs_created']}")
    print(f"  Symlinks created:     {stats['symlinks_created']}")

    missing = sorted(set(stats["missing_sources"]))
    if missing:
        print(f"\n  Missing sources ({len(missing)}):")
        for m in missing:
            print(f"    [MISSING] {m}")
    else:
        print("\n  All source files found!")
    print("=" * 60)


if __name__ == "__main__":
    main()
