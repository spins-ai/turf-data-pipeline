def create_post_course():
    d = ROOT / "pipeline" / "data" / "post_course"
    ensure_dir(d)
    entries = []

    # Predictions + actuals placeholders
    for name in ("predictions", "actuals"):
        sub = d / name
        ensure_dir(sub)
        (sub / ".gitkeep").touch()
        entries.append((f"{name}/", "(generated later)", True))

    # Labels (actual outcomes)
    entries.append(symlink_dir("output/labels", d, "labels"))

    # Rapports for result verification
    entries.append(symlink_file("output/21_rapports_definitifs/rapports_definitifs.json", d))

