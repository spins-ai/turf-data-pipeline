#!/usr/bin/env python3
"""
organize_model_data.py
Creates model-specific data folders under pipeline/data/ with RELATIVE symlinks
pointing to actual data files in output/. No data is copied.

Each model type gets its own folder with all the data it needs.
"""

import os
import sys
from pathlib import Path

# ── Project root = directory containing this script ──────────────────────────
ROOT = Path(__file__).resolve().parent


# ── Helper functions ─────────────────────────────────────────────────────────

def ensure_dir(path: Path):
    path.mkdir(parents=True, exist_ok=True)


def make_relative_symlink(target: Path, link: Path):
    """
    Create a relative symlink at `link` pointing to `target`.
    Both paths must be absolute. The symlink value stored on disk
    will be the *relative* path from the link's parent to the target.
    """
    if link.exists() or link.is_symlink():
        link.unlink()

    target = target.resolve()
    if not target.exists():
        print(f"  [SKIP] target missing: {target}")
        return False

    rel = os.path.relpath(target, link.parent)
    os.symlink(rel, link)
    return True


def symlink_file(src_relative: str, dest_dir: Path, dest_name: str = None):
    """
    Symlink a single file.
    src_relative is relative to ROOT (e.g. 'output/features/features_matrix.json').
    """
    src = ROOT / src_relative
    name = dest_name or src.name
    link = dest_dir / name
    ok = make_relative_symlink(src, link)
    return (name, src_relative, ok)


def symlink_dir(src_relative: str, dest_dir: Path, dest_name: str = None):
    """Symlink an entire directory."""
    src = ROOT / src_relative
    name = dest_name or src.name
    link = dest_dir / name
    ok = make_relative_symlink(src, link)
    return (name, src_relative, ok)


def write_readme(dest_dir: Path, title: str, description: str, entries: list):
    """Write a README.txt listing what's in the folder."""
    lines = [
        f"{'=' * 60}",
        f"  {title}",
        f"{'=' * 60}",
        "",
        description,
        "",
        "Contents (symlinks to source):",
        "-" * 40,
    ]
    for name, source, ok in entries:
        status = "OK" if ok else "MISSING"
        lines.append(f"  {name:40s} <- {source}  [{status}]")

    lines += [
        "",
        "-" * 40,
        "All entries are SYMLINKS — no data is duplicated.",
        f"Generated by organize_model_data.py",
        "",
    ]
    (dest_dir / "README.txt").write_text("\n".join(lines), encoding="utf-8")


# ── Data folder definitions ─────────────────────────────────────────────────

def create_baseline_ml():
    d = ROOT / "pipeline" / "data" / "baseline_ml"
    ensure_dir(d)
    entries = []

    # Features matrix (all formats)
    for ext in ("json", "csv", "parquet"):
        entries.append(symlink_file(f"output/features/features_matrix.{ext}", d))

    # Labels directory
    entries.append(symlink_dir("output/labels", d, "labels"))

    # Courses & partants
    entries.append(symlink_file("output/02_liste_courses/courses_normalisees.json", d))
    entries.append(symlink_file("output/02_liste_courses/partants_normalises.json", d))

    # Rapports
    entries.append(symlink_file("output/21_rapports_definitifs/rapports_definitifs.json", d))
    entries.append(symlink_file("output/38_rapports_internet/rapports_internet.json", d))

    # Meteo
    entries.append(symlink_file("output/13_meteo_historique/meteo_historique.json", d))

    # Pedigree (both sources)
    entries.append(symlink_dir("output/14_pedigree", d, "pedigree_14"))
    entries.append(symlink_dir("output/08_pedigree", d, "pedigree_08"))

    # Hippodromes DB
    entries.append(symlink_file("hippodromes_db.py", d))

    write_readme(d, "BASELINE ML DATA",
                 "Shared tabular data for XGBoost, LightGBM, CatBoost, RandomForest, LogisticRegression.",
                 entries)
    return ("baseline_ml", d, entries)


def create_deep_learning():
    d = ROOT / "pipeline" / "data" / "deep_learning"
    ensure_dir(d)
    entries = []

    # Everything from baseline_ml (symlink to the baseline_ml folder itself)
    baseline = ROOT / "pipeline" / "data" / "baseline_ml"
    entries.append(("baseline_ml", "pipeline/data/baseline_ml",
                    make_relative_symlink(baseline, d / "baseline_ml")))

    # Performances detaillees — historical race sequences
    entries.append(symlink_file(
        "output/22_performances_detaillees/performances_detaillees.json", d))

    write_readme(d, "DEEP LEARNING DATA",
                 "For LSTM, GRU, TabNet, TFT.\n"
                 "Includes everything from baseline_ml/ PLUS temporal sequence data.\n"
                 "The baseline_ml/ symlink gives access to all tabular features.",
                 entries)
    return ("deep_learning", d, entries)


